# 解决四个现代化会分开的问题
jieba.add_word('四个现代化', freq=1000, tag='n')

def preprocess_text(text):
    """文本预处理函数"""
    text = re.sub(r'[^\u4e00-\u9fa5a-zA-Z0-9]', ' ', text)
    return re.sub(r'\s+', ' ', text).strip()
